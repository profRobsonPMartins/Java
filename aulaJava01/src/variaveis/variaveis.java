package variaveis;

public class variaveis {
    
    public static void main(String[] args) {
        
        //Declarar variaveis
        // Tipo inteiro
        int idade = 34;
        // Imprimir
        System.out.println("A minha idade é: " + idade);

        // tipo string (conjunto de caracteres)
        String nome = "Robson";
        System.out.println("Meu nome é: " + nome);

        // Tipo double 
        double altura = 1.75;
        System.out.println("A minha altura é: " + altura);
        

    }
}
